#include <stdio.h>

void main()
{
   printf("Programa que apresenta os Caracteres Especiais\n\n");
   printf("\\n\t-\t<ENTER>\n");  /* Saída: \n   -  <ENTER> */
   printf("\\\\\t-\t\\\n");      /* Saída: \\   -  \ */
   printf("\\t\t-\t<TAB>\n");    /* Saída: \t   -  <TAB> */
   printf("\%%%\t-\t%\n");         /* Saída: %%  -  % */ 
}
